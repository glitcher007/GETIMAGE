/***BINARY SEARCH IN ARRAY***/
ll binarySearch(ll arr[], ll l, ll r, ll x)
{
    while (l <= r) {
        ll m = l + (r - l) / 2;
 
         if (arr[m] == x)
            return m;
 
        if (arr[m] < x)
            l = m + 1;
         else
            r = m - 1;
    }
    return -1;
}
 
